CREATE TABLE IF NOT EXISTS restore_upload (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    original_name VARCHAR(500) NOT NULL,
    file_path VARCHAR(1000) NOT NULL,
    file_size BIGINT NOT NULL,
    checksum_sha256 VARCHAR(64) NOT NULL,
    file_format VARCHAR(40) NOT NULL,
    source_db_type VARCHAR(40),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_restore_upload_expires ON restore_upload(expires_at);

CREATE TABLE IF NOT EXISTS restore_job (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_kind VARCHAR(20) NOT NULL,
    source_id BIGINT NOT NULL,
    source_name VARCHAR(500),
    source_file_path VARCHAR(1000) NOT NULL,
    source_checksum VARCHAR(64) NOT NULL,
    file_format VARCHAR(40) NOT NULL,
    source_db_type VARCHAR(40),
    target_connection_id BIGINT NOT NULL,
    target_db_type VARCHAR(40) NOT NULL,
    conflict_mode VARCHAR(20) NOT NULL,
    namespace_mapping CLOB,
    status VARCHAR(40) NOT NULL,
    phase VARCHAR(80),
    progress_current BIGINT,
    progress_total BIGINT,
    message CLOB,
    cancel_requested BOOLEAN DEFAULT FALSE,
    actor VARCHAR(120),
    started_at TIMESTAMP,
    finished_at TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_restore_job_target_created ON restore_job(target_connection_id, created_at);

CREATE TABLE IF NOT EXISTS sql_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    connection_id BIGINT NOT NULL,
    sql_text CLOB NOT NULL,
    sql_type VARCHAR(40) NOT NULL,
    status VARCHAR(40) NOT NULL,
    elapsed_ms BIGINT DEFAULT 0,
    error_message CLOB,
    actor VARCHAR(120),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_sql_history_connection_id ON sql_history(connection_id, id);
