CREATE INDEX IF NOT EXISTS idx_backup_task_connection_id ON backup_task(connection_id);

CREATE TABLE IF NOT EXISTS backup_task_table (
    task_id BIGINT NOT NULL,
    target_order INT NOT NULL,
    table_name VARCHAR(240) NOT NULL,
    PRIMARY KEY (task_id, target_order),
    CONSTRAINT uk_backup_task_table_name UNIQUE (task_id, table_name),
    CONSTRAINT fk_backup_task_table_task FOREIGN KEY (task_id) REFERENCES backup_task(id) ON DELETE CASCADE
);

INSERT INTO backup_task_table(task_id, target_order, table_name)
SELECT task.id, 0, task.table_name
FROM backup_task task
WHERE UPPER(task.scope) = 'TABLE'
  AND task.table_name IS NOT NULL
  AND NOT EXISTS (SELECT 1 FROM backup_task_table target WHERE target.task_id = task.id);

UPDATE backup_task SET scope = 'TABLES' WHERE UPPER(scope) = 'TABLE';

